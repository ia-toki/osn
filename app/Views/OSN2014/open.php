    <h3>
        <a href="https://toki.id/2014/osn-informatika-terbuka-2014/" target="blank">OSN Informatika Terbuka 2014</a>
    </h3>

    <img src="/images/openosn2015.png" width="250">

    <p><strong>OSN Informatika Terbuka 2014</strong> (untuk selanjutnya disebut OSN Terbuka) adalah olimpiade yang berbentuk kontes pemrograman yang diadakan bersamaan dengan OSN 2014 bidang informatika yang diadakan di Mataram, Nusa Tenggara Barat (untuk selanjutnya disebut OSN Informatika 2014).</p>
    <p>Tujuan diadakannya OSN Terbuka ini adalah untuk memberikan kesempatan kepada peserta yang karena satu dan lain hal tidak diundang ke OSN Informatika 2014. OSN Terbuka dilaksanakan dengan menggunakan prosedur dan aturan yang serupa dengan OSN Informatika 2014.</p>
    <p>OSN Terbuka dilaksanakan secara online. Kontes dapat diikuti dari tempat masing-masing peserta. OSN Terbuka dapat diikuti oleh siapa pun yang telah memenuhi prosedur pendaftaran. OSN Terbuka akan dibagi menjadi tiga kategori, <strong>kategori SMA</strong>, <strong>kategori Umum</strong>, dan <strong>kategori Internasional</strong>. Pemisahan ini bertujuan untuk mengetahui kemampuan siswa/i SMA/ sederajat yang berkompetisi di OSN Terbuka dan mendapatkan penilaian yang bersih (terpisah dari kategori yang sudah lulus SMA). Untuk Anda yang sudah tidak duduk di bangku SMA, bisa mengikuti OSN Terbuka kategori Umum. Sedangkan kategori Internasional dikhususkan bagi peserta yang berasal dari luar Indonesia.</p>

    <hr />

    <h3><strong>Prosedur Pendaftaran Kategori SMA</strong></h3>

    <p>Pendaftaran dilakukan di <a href="#">formulir berikut</a>. Data yang dimasukkan harus jelas dan bisa dipertanggung jawabkan. Setelah mengisi formulir pendaftaran, calon peserta diminta mengirimkan bukti identitas diri berupa scan surat keterangan siswa aktif dari sekolah (format bebas) ke <a href="#">alumni@toki.or.id</a> dengan subjek email <strong>[OPEN OSN] Pendaftaran Kategori SMA</strong>. Para calon peserta yang telah mengisi formulir tersebut dan mengirimkan bukti identitas diri, akan mendapatkan email konfirmasi mengenai status pendaftaran.</p>

    <hr />

    <h3><strong>Prosedur Pendaftaran Kategori Umum</strong></h3>

    <p>Pendaftaran dilakukan di <a href="#">formulir berikut</a>. Data yang dimasukkan harus jelas dan bisa dipertanggung jawabkan. Setelah mengisi formulir pendaftaran, calon peserta diminta mengirimkan bukti identitas diri berupa scan KTP/SIM/KTM/Surat Keterangan Mahasiswa Aktif dari Universitas ke <a href="#">alumni@toki.or.id</a> dengan subjek email <strong>[OPEN OSN] Pendaftaran Kategori Umum</strong>. Para calon peserta yang telah mengisi formulir tersebut dan mengirimkan bukti identitas diri, akan mendapatkan email konfirmasi mengenai status pendaftaran.</p>
    <p><strong>Diharapkan bagi peserta untuk tidak mendaftarkan diri pada lebih dari satu kontes secara bersamaan untuk mempermudah administrasi pengurusan data peserta.</strong></p>

    <hr />

    <p>Teknis pelaksanaan OSN Terbuka akan menyerupai OSN Informatika 2014 di Mataram (untuk itu harap terlebih dahulu membaca <a href="#">dokumen Teknis OSN Informatika 2014</a>). OSN Terbuka akan dilaksanakan secara serentak dengan OSN Informatika 2014 dengan jadwal sebagai berikut:</p>

    <ul>
        <li>Batas Pendaftaran : 30 Agustus 2014, pukul 23:59 WIB (GMT +7)</li>
        <li>Sesi Latihan (Hari 0) : 2 September 2014, <strong>pukul 13.00 WIB (GMT +7)</strong></li>
        <li>Kontes (Hari 1) : 3 September 2014, <strong>pukul 09.00 WIB (GMT +7)</strong></li>
        <li>Kontes (Hari 2) : 4 September 2014, <strong>pukul 09.00 WIB (GMT +7)</strong></li>
    </ul>

    <p>Untuk informasi lebih lengkap silahkan membaca <a href="#">dokumen sosialisasi OSN Terbuka</a>. Pertanyaan mengenai OSN Terbuka dapat dikirim melalui email ke <a href="#">alumni@toki.or.id</a> dengan subjek email mengandung tag <strong>[OPEN OSN]</strong>.</p>
